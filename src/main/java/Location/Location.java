package Location;

public class Location {
	private int ID;
	private double LAT;
	private double LNT;
	private String DATE;
	
	public int getID() {
		return ID;
	}
	public void setID(int iD) {
		ID = iD;
	}
	public double getLAT() {
		return LAT;
	}
	public void setLAT(double lAT) {
		LAT = lAT;
	}
	public double getLNT() {
		return LNT;
	}
	public void setLNT(double lNT) {
		LNT = lNT;
	}
	public String getDATE() {
		return DATE;
	}
	public void setDATE(String dATE) {
		DATE = dATE;
	}
	
	
}
